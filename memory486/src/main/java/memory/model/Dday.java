package memory.model;

import java.util.Date;

public class Dday {
	int day_no;
  	String day_title;
  	Date day_date;
  	String day_id;
	public int getDay_no() {
		return day_no;
	}
	public void setDay_no(int day_no) {
		this.day_no = day_no;
	}
	public String getDay_title() {
		return day_title;
	}
	public void setDay_title(String day_title) {
		this.day_title = day_title;
	}
	public Date getDay_date() {
		return day_date;
	}
	public void setDay_date(Date day_date) {
		this.day_date = day_date;
	}
	public String getDay_id() {
		return day_id;
	}
	public void setDay_id(String day_id) {
		this.day_id = day_id;
	}

}
